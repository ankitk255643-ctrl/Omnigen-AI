// Initialize the AI client
// Note: process.env.GEMINI_API_KEY is injected by the platform
// We initialize it inside functions to ensure fresh API key usage
const getApiKey = () => {
  const manualKey = typeof window !== 'undefined' ? localStorage.getItem('OMNIGEN_CUSTOM_KEY') : null;
  return manualKey || process.env.GEMINI_API_KEY || "";
};

const getAimlKey = () => {
  const manual = typeof window !== 'undefined' ? localStorage.getItem('OMNIGEN_AIML_KEY') : null;
  return manual || "ab331efe3369c7d6fbf60941b60d5f94"; // Use provided key as default
};

const getJson2videoKey = () => {
  return typeof window !== 'undefined' ? localStorage.getItem('OMNIGEN_JSON2VIDEO_KEY') : null;
};

export const models = {
  text: "gemini-3-flash-preview",
  pro: "gemini-3.1-pro-preview",
  image: "gemini-3.1-flash-image-preview",
  video: "veo-3.1-lite-generate-preview",
  aiml: "claude-opus-4-6",
  seedance: "bytedance/seedance-1-0-lite-t2v",
  sora: "sora-2-t2v"
};

export async function generateText(prompt: string, systemInstruction?: string) {
  const aimlKey = getAimlKey();
  
  if (aimlKey) {
    try {
      const client = new OpenAI({
        apiKey: aimlKey,
        baseURL: "https://ai.aimlapi.com",
        dangerouslyAllowBrowser: true
      });
      
      const response = await client.chat.completions.create({
        model: models.aiml,
        messages: [
          ...(systemInstruction ? [{ role: "system" as const, content: systemInstruction }] : []),
          { role: "user" as const, content: prompt }
        ],
      });
      
      return response.choices[0].message.content || "No response from AIML API";
    } catch (error) {
      console.error("AIML API Error, falling back to Gemini:", error);
    }
  }

  const ai = new GoogleGenAI({ apiKey: getApiKey() });
  const response = await ai.models.generateContent({
    model: models.text,
    contents: prompt,
    config: {
      systemInstruction,
    },
  });
  return response.text;
}

export async function generateImage(prompt: string, referenceImage?: string) {
  const ai = new GoogleGenAI({ apiKey: getApiKey() });
  const parts: any[] = [{ text: prompt }];
  
  if (referenceImage) {
    const base64Data = referenceImage.split(',')[1] || referenceImage;
    const mimeType = referenceImage.match(/^data:(.*);base64,/)?.[1] || 'image/jpeg';
    parts.unshift({ inlineData: { data: base64Data, mimeType } });
  }

  const response = await ai.models.generateContent({
    model: models.image,
    contents: {
      parts,
    },
    config: {
      imageConfig: {
        aspectRatio: "1:1",
        imageSize: "1K"
      }
    }
  });

  for (const part of response.candidates?.[0]?.content?.parts || []) {
    if (part.inlineData) {
      return `data:${part.inlineData.mimeType};base64,${part.inlineData.data}`;
    }
  }
  throw new Error("No image generated by the AI model.");
}

export async function generateVideo(prompt: string, imageBase64?: string) {
  const json2videoKey = getJson2videoKey();
  const aimlKey = getAimlKey();
  
  if (json2videoKey) {
    try {
      // JSON2Video API call
      const response = await fetch('https://api.json2video.com/v2/jobs', {
        method: 'POST',
        headers: {
          'x-api-key': json2videoKey,
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({
          "scenes": [
            {
              "elements": [
                {
                  "type": "text",
                  "text": prompt,
                  "duration": 5
                }
              ]
            }
          ]
        }),
      });

      const data = await response.json();
      if (!response.ok) throw new Error(data.message || "JSON2Video API Error");

      const jobId = data.project;
      
      // Polling for JSON2Video
      let status = 'pending';
      let videoUrl = '';
      while (status !== 'completed' && status !== 'error') {
        await new Promise(resolve => setTimeout(resolve, 5000));
        const pollRes = await fetch(`https://api.json2video.com/v2/jobs/${jobId}`, {
          headers: { 'x-api-key': json2videoKey }
        });
        const pollData = await pollRes.json();
        status = pollData.status;
        videoUrl = pollData.url;
        if (status === 'error') throw new Error("JSON2Video generation failed");
      }
      return videoUrl;
    } catch (error) {
      console.error("JSON2Video Error, falling back to AIML:", error);
    }
  }

  if (!aimlKey) {
    throw new Error("AIML API Key or JSON2Video Key is required for video generation.");
  }

  try {
    const response = await fetch('https://ai.aimlapi.com/v1/video/generations', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${aimlKey}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        "model": models.sora,
        "prompt": prompt
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      throw new Error(`AIML API Error: ${response.status} - ${errorText}`);
    }

    const data = await response.json();
    
    // Handle direct URL if available
    if (data.url) return data.url;
    if (data.output && data.output.url) return data.output.url;

    const taskId = data.id || data.task_id;
    if (!taskId) {
      throw new Error("No task ID returned from AIML API");
    }

    // Polling logic for v1
    const timeout = 600 * 1000; // 10 minutes
    const startTime = Date.now();

    while (Date.now() - startTime < timeout) {
      const pollRes = await fetch(`https://ai.aimlapi.com/v1/video/generations/${taskId}`, {
        headers: {
          'Authorization': `Bearer ${aimlKey}`,
          'Content-Type': 'application/json',
        }
      });

      if (!pollRes.ok) {
        throw new Error(`Polling failed: ${pollRes.status}`);
      }

      const pollData = await pollRes.json();
      const status = pollData.status;

      if (status === 'completed' || status === 'success') {
        return pollData.url || (pollData.output && pollData.output.url) || pollData.video_url;
      }

      if (['failed', 'error', 'cancelled'].includes(status)) {
        throw new Error(`Video generation failed with status: ${status}`);
      }

      // Still processing
      await new Promise((resolve) => setTimeout(resolve, 10000));
    }

    throw new Error("Video generation timed out");
  } catch (error) {
    console.error("AIML Video Error:", error);
    throw error;
  }
}

export async function analyzeMedia(prompt: string, mediaData: string, mimeType: string) {
  const ai = new GoogleGenAI({ apiKey: getApiKey() });
  const base64Data = mediaData.split(',')[1] || mediaData;
  const response = await ai.models.generateContent({
    model: models.text,
    contents: {
      parts: [
        { inlineData: { data: base64Data, mimeType } },
        { text: prompt }
      ]
    }
  });
  return response.text;
}

