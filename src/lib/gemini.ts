export async function detectIntent(prompt: string, fileInfo?: { name: string, type: string }) {
  const ai = new GoogleGenAI({ apiKey: getApiKey() });
  const systemInstruction = `
You are an intent detection engine for OmniGen AI.
Analyze the user's prompt and file information to determine the required action and tool.
Return ONLY a JSON object in this format:
{
  "action": "string",
  "input_type": "string",
  "output_type": "string",
  "tool": "string",
  "confidence": number,
  "reasoning": "string"
}

Available tools:
- text-to-image
- text-to-video
- text-to-script
- text-to-prompt
- text-to-code
- image-to-text
- video-to-text
- image-to-video
- text-to-pdf
- word-to-pdf
- image-merger
- bg-remover
- text-to-song
- watermark-remover
- pdf-editor
- whatsapp-agent
- file-converter
- ai-assistant (default for general chat)

Example: "Make a video of a cat" -> { "action": "generate", "input_type": "text", "output_type": "video", "tool": "text-to-video", "confidence": 0.95 }
`;

  const input = `Prompt: "${prompt}"\nFile: ${fileInfo ? `${fileInfo.name} (${fileInfo.type})` : 'None'}`;
  
  const response = await ai.models.generateContent({
    model: models.text,
    contents: input,
    config: {
      systemInstruction,
      responseMimeType: "application/json"
    },
  });

  try {
    return JSON.parse(response.text);
  } catch (e) {
    return { tool: 'ai-assistant', action: 'chat', confidence: 0.5 };
  }
}
